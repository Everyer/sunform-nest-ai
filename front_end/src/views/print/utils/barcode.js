// 特殊符号值定义
const START_CODE_B = 104; // START B
const STOP_CODE = 106;    // STOP

// 字符映射：从 ASCII 到 Code128 索引值（B型）
function getCharacterValue(char) {
  const code = char.charCodeAt(0);
  if (code >= 32 && code <= 126) {
    return code - 32; // 标准 ASCII 32 - 126 直接映射为 0 - 94
  }
  return -1; // 不支持的字符，降级处理
}

export const BarcodeAlg = {
  /**
   * 将输入文本进行 Code128B 编码，输出 0/1 序列
   * @param {string} text 需要编码的字符串 (仅支持常规ASCII字符)
   * @returns {string} 由 "0" 和 "1" 组成的条形码二进制序列
   */
  encode128: function(text) {
    if (!text) return "";
    
    // 1. 初始化结果，写入 START B
    let binSequence = CODE128_B_TABLE[START_CODE_B];
    let checksum = START_CODE_B;
    
    // 2. 依次加入字符并计算校验和
    for (let i = 0; i < text.length; i++) {
      const charVal = getCharacterValue(text[i]);
      if (charVal === -1) continue; // 过滤不支持的字符
      
      binSequence += CODE128_B_TABLE[charVal];
      checksum += charVal * (i + 1);
    }
    
    // 3. 计算校验符并添加
    const checkSymbolVal = checksum % 103;
    binSequence += CODE128_B_TABLE[checkSymbolVal];
    
    // 4. 添加结束符 (STOP) 及其终止符（1100011101011 + 11 即 13位）
    binSequence += "110001110101111"; 
    
    return binSequence;
  },

  /**
   * 将条码二进制序列转换为 SVG 元素渲染所需的条(Bar)列表数据
   * @param {string} text 输入的文本
   * @returns {Array<{x: number, width: number}>} 条的相对定位列表，单位是相对单位宽度
   */
  getSvgBars: function(text) {
    const binStr = this.encode128(text);
    if (!binStr) return [];
    
    const bars = [];
    let curWidth = 0;
    let xOffset = 0;
    
    for (let i = 0; i < binStr.length; i++) {
      if (binStr[i] === '1') {
        curWidth++;
      } else {
        if (curWidth > 0) {
          bars.push({ x: xOffset - curWidth, width: curWidth });
          curWidth = 0;
        }
      }
      xOffset++;
    }
    // 补齐最后一个条
    if (curWidth > 0) {
      bars.push({ x: xOffset - curWidth, width: curWidth });
    }
    
    return {
      totalWidth: binStr.length,
      bars: bars
    };
  }
};

export default BarcodeAlg;
