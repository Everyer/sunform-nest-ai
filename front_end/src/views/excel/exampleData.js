function makeCell (v, t) {
  // t: 1=string, 2=number, 3=boolean, 4=date
  if (t === undefined) {
    if (typeof v === 'number') t = 2
    else if (typeof v === 'boolean') t = 3
    else t = 1
  }
  return { v, t, s: undefined }
}

